//devuelve el color segun linea y recorrido
function colorPuntos(d) { 
	return d == "A_IDA" ? '#64d637' : 
	d == "A_VUELTA" ? '#4cb224' :

        d == "E_VUELTA" ? '#2493b2' :
        d == "E_IDA" ? '#37b3d6' : 

        d == "OESTE_VUELTA" ? '#8b24b2' : 
        d == "OESTE_IDA" ? '#aa37d6' : 

        d == "ESTE_VUELTA" ? '#b24424' : 
        d == "ESTE_IDA" ? '#d65b37' :  
	'#000000'; 
};

function estilo_Rec (feature) {
        if(feature.properties.recorrido=="IDA"){
                return  {
                        color: colorPuntos(feature.properties.name), 
                        weight: 3.5,
                        opacity : 0.8,
                        stroke: "#000"
                };  
                           
        }else{ //CASO DE VUELTA
                return{
                        color: colorPuntos(feature.properties.name), 
                        lineCap: 'square',
                        weight: 3.5,
                        opacity : 1,
                        dashArray: '12, 8'
                };
        }
        
};

//Estilo de los circulos (inicio-final recorrido)
function estiloCircleMarker(feature, latlng) {
	return L.circleMarker(latlng, {
		radius: 6,
		fillColor: colorPuntos(feature.properties.name),
		color: '#000',
		weight: 1.5,
		opacity: 1.0,
		fillOpacity: 1.0
	})
}

//Datos al popup del recorrido
function popUpInfo(feature, layer) {
        if (feature.properties && feature.properties.name) {
            layer.bindPopup(`
            <ul id="listaPopUp">
            <li class='li-TituloPri'>INFORMACIÓN</li>
            <li class='li-Titulo'>Recorrido:</li>
            <li class='li-Dato'>${feature.properties.nombre}</li>
            <li class='li-Titulo'>Inicio:</li>
            <li class='li-Dato'>${feature.properties.inicio}</li>
            <li class='li-Titulo'>Final:</li>
            <li class='li-Dato'>${feature.properties.final}</li>
            <li class='li-Titulo'>Frecuencia:</li>
            <li class='li-Dato'>${feature.properties.frecuencia}</li>
            </ul>`);
        }
}

